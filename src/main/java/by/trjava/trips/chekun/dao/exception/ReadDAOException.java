package by.trjava.trips.chekun.dao.exception;

public class ReadDAOException extends Exception {

    public ReadDAOException() {
        super();
    }

    public ReadDAOException(String message) {
        super(message);
    }

    public ReadDAOException(Throwable cause) {
        super(cause);
    }

    public ReadDAOException(String message, Throwable cause) {
        super(message, cause);
    }
}
