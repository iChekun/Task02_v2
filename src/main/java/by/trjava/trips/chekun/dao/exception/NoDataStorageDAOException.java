package by.trjava.trips.chekun.dao.exception;

public class NoDataStorageDAOException extends Exception {

    public NoDataStorageDAOException() {
        super();
    }

    public NoDataStorageDAOException(String message) {
        super(message);
    }

    public NoDataStorageDAOException(Throwable cause) {
        super(cause);
    }

    public NoDataStorageDAOException(String message, Throwable cause) {
        super(message, cause);
    }
}
