package by.trjava.trips.chekun.domain.util.exception;

public class ReadRestrictionsException extends Exception {

    public ReadRestrictionsException() {
        super();
    }

    public ReadRestrictionsException(String message) {
        super(message);
    }

    public ReadRestrictionsException(Throwable cause) {
        super(cause);
    }

    public ReadRestrictionsException(String message, Throwable cause) {
        super(message, cause);
    }
}

